
# in for loops, we covered how to
# make code repeat a set amount of
# times. Now, we need to know how to
# loop until a condition is no longer true

# one specific exmample I can provide
# that we will use in the near future:

game_over = False
while not game_over:  # "not" reverses the boolean, False -> True
    pass  # do something

# this will repeat until until game_over
# turns out true, so then the condition
# not game_over is False and the game stops

# we can also make infinite loops:

while True:
    pass  # oh yea btw this pass keyword means "ignore me"

